
public class Sum {
	
	public int sumOne(int n) {
		int sum = 0;
		for ( int i = 0; i < n; i++ ) {
			sum++;
		}
		return sum;
	}
	
	public int sumTwo(int n) {
		int sum = 0;
		for ( int i = 0; i < n; i++ ) {
			for ( int j = 0; j < n; j++ ) {
				sum++;
			}
		}
		return sum;
	}
	
	public int sumThree(int n) {
		int sum = 0;
        for ( int i = 0; i < n; i++ ) {
            for ( int j = 0; j < n * n; j++ ) {
                sum++;
            }
        }
        return sum;
	}

	public int sumFour(int n) {
		int sum = 0;
        for ( int i = 0; i < n; i++ ) {
            for ( int j = 0; j < i; j++ ) {
                sum++;
            }
        }
        return sum;
	}
	
	public int sumFive(int n) {
		int sum = 0;
		for ( int i = 0; i < n; i++ ) {
			for ( int j = 0; j < (i * i); j++ ) {
		    	 for (int k = 0; k < j; k++ ) {
		                    	   		sum++;
		    	 }
			}
		}
		return sum;
	}
		
	public int sumSix(int n) {
		int sum = 0;
		for ( int i = 1; i < n; i++ ) {
			for ( int j = 1; j < i * i; j++ ) {
				if ( j % i == 0 ) {
					for ( int k = 0; k < j; k++ ) {
		                              sum++;
					}
				}
			}
		}
		return sum;
	}
	


}
